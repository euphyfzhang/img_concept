CREATE OR REPLACE TABLE IMG_RECG.FEEDBACK
COPY GRANTS
(
    _ID INTEGER AUTOINCREMENT ORDER
    , _LOAD_TS TIMESTAMP_NTZ DEFAULT SYSDATE()
    , REQUEST_ID VARCHAR
    , RATING VARCHAR
    , FEEDBACK_MESSAGE VARCHAR
    , PRIMARY KEY (_ID)
);

CREATE OR REPLACE TABLE IMG_RECG.CHAT_MESSAGE
COPY GRANTS
(
    _ID INTEGER AUTOINCREMENT ORDER
    , _LOAD_TS TIMESTAMP_NTZ DEFAULT SYSDATE()
    , REQUEST_ID VARCHAR
    , MESSAGE VARCHAR
    , SUGGESTION VARIANT
    , SQL VARCHAR
    , CONFIDENCE VARCHAR
    , PRIMARY KEY (_ID)
);